    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        try {
            if (!txtId.getText().isEmpty()) {
                String id = txtId.getText();
                String elimanted = doctorI.delete(id);
                JOptionPane.showMessageDialog(this, elimanted);
            } else {
                JOptionPane.showMessageDialog(this, "Debe ingresar el ID para eliminar!!!", "Error", 2);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al buscar: " + e.getMessage(), "Error", 2);
            //Nota el 2 significa JOptionPane.ERROR_MESSAGE/WARNING_MESSAGE (2): Muestra un ícono de error/advertencia.
        }
        Clean.clearSpaces(txtId, txtName, txtLastName, txtPhone, txtEmail, txtDirection, txtBirthDay,
                            txtSpeciality);
    }//GEN-LAST:event_btnDeleteActionPerformed

