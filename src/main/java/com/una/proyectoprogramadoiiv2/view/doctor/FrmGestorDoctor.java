    private void btnModifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifyActionPerformed
        // TODO add your handling code here:
        try {
            if (verify()) {
                try {
                    LocalDate birthDate = DateFormatUtil.parseDate(txtBirthDay.getText());
                    String[] updatedData = {
                        txtId.getText(),
                        txtName.getText(),
                        txtLastName.getText(),
                        txtPhone.getText(),
                        txtEmail.getText(),
                        txtDirection.getText(),
                        birthDate.toString(),
                        txtSpeciality.getText()
                    };
                    String updateResponse = doctorI.modify(updatedData, txtId.getText());
                    JOptionPane.showMessageDialog(this, updateResponse);
                    if (updateResponse.equals("Actualizado exitosamente!!!")) {
                        Clean.clearSpaces(txtId, txtName, txtLastName, txtPhone, txtEmail, txtDirection, txtBirthDay,
                            txtSpeciality);
                    }
                } catch (DateTimeParseException e) {
                    JOptionPane.showMessageDialog(this, "Formato de fecha incorrecto. Use el formato dd-MM-yyyy, por favor!!!.", "Error", 2);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Debe llenar todos los campos!!!", "Error", 2);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al modificar!!!: " + e.getMessage(), "Error", 2);
        }
    }//GEN-LAST:event_btnModifyActionPerformed
    //Esto sirve en el de modificar

    private boolean verify() {
        if (!txtId.getText().equals("") && !txtName.getText().equals("")
                && !txtLastName.getText().equals("") && !txtPhone.getText().equals("")
                && !txtEmail.getText().equals("") && !txtBirthDay.getText().equals("")
                && !txtSpeciality.getText().equals("")) {
            return true;
        }
        return false;
    }



