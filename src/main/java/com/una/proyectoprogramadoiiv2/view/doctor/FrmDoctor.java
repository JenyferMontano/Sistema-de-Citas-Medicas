    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (verify()) {
            try {
                String data[] = new String[8];
                data[0] = txtId.getText();
                data[1] = txtName.getText();
                data[2] = txtLastName.getText();
                data[3] = txtPhone.getText();
                data[4] = txtEmail.getText();
                data[5] = txtDirection.getText();
                LocalDate birthdate = DateFormatUtil.parseDate(txtBirthDay.getText());
                data[6] = birthdate.toString();

                data[7] = txtSpeciality.getText();
                String msj = doctorI.add(data);
                JOptionPane.showMessageDialog(this, msj);
            } catch (DateTimeParseException e) {
                JOptionPane.showMessageDialog(this, "Formato de fecha incorrecto. Por favor, use dd-MM-yyyy.", "Error", 2);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error al agregar el doctor: " + e.getMessage(), "Error", 2);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debe completar todos los campos!!!", "Error", 2);
        }
    }//GEN-LAST:event_btnAddActionPerformed

