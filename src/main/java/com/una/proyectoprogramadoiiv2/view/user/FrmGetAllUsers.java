    private boolean getAll(){
        String data[][] = user.getAll();
        if (data != null) {
            for (String[] usuario : data) {
            dtm.addRow(usuario);
            }
                return true;
            }
        
           return false;
    }
    
    private void clearTable() {
        dtm.setRowCount(0); //limpia el contenido de una tabla
    }
    
    
    
    
    

