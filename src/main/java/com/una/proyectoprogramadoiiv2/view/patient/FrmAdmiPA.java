    private void btnModifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifyActionPerformed
        // TODO add your handling code here:
        try {
            if (verify()) {
                try {
                    LocalDate birthDate = LocalDate.parse(txtBirth.getText());
                    String[] updatedData = {
                        txtID.getText(),
                        txtName.getText(),
                        txtLastName.getText(),
                        txtPhone.getText(),
                        txtEmail.getText(),
                        txtDirection.getText(),
                        birthDate.toString(),
                        txtPContactEmergency.getText(),
                        txtTContactEmergency.getText()
                    };
                    String updateResponse = pat.update(updatedData, txtID.getText());
                    JOptionPane.showMessageDialog(this, updateResponse);
                    if (updateResponse.equals("Actualizado exitosamente!!!")) {
                    }
                    Clean.clearSpaces(txtID, txtName, txtLastName, txtPhone, txtEmail, txtDirection, txtBirth,
                            txtPContactEmergency, txtTContactEmergency);
                } catch (DateTimeParseException e) {
                    JOptionPane.showMessageDialog(this, "Formato de fecha incorrecto. Use el formato AAAA-MM-DD, por favor!!!.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Debe llenar todos los campos!!!", "Error", 2);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al modificar!!!: " + e.getMessage(), "Error", 2);
        }

    }//GEN-LAST:event_btnModifyActionPerformed


    private void btnValidateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValidateActionPerformed
        // TODO add your handling code here:
        try {
            if (!txtID.getText().isEmpty()) {
                String id = txtID.getText();
                String[] doc = pat.find(id);
                if (doc != null) {
                    if (doc.length > 1) {
                        txtName.setText(doc[1]);
                        txtLastName.setText(doc[2]);
                        txtPhone.setText(doc[3]);
                        txtEmail.setText(doc[4]);
                        txtDirection.setText(doc[5]);
                        txtBirth.setText(doc[6]);
                        txtPContactEmergency.setText(doc[7]);
                        txtTContactEmergency.setText(doc[8]);
                    } else {
                        JOptionPane.showMessageDialog(this, "Error en los datos del paciente", "Error", 2);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "No existe!!!", "Error", 2);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Debe ingresar el ID para buscar!!!", "Error", 2);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al buscar: " + ex.getMessage(), "Error", 2);
        }
    }//GEN-LAST:event_btnValidateActionPerformed

