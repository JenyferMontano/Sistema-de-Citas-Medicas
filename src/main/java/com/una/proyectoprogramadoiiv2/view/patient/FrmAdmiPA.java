    private boolean verify() {
        if (!txtID.getText().isEmpty() && !txtName.getText().isEmpty() && !txtLastName.getText().isEmpty() && !txtPhone.getText().isEmpty()
                && !txtEmail.getText().isEmpty() && !txtEmail.getText().isEmpty() && !txtDirection.getText().isEmpty()
                && !txtBirth.getText().isEmpty() && !txtPContactEmergency.getText().isEmpty() && !txtTContactEmergency.getText().isEmpty()) {
            return true;
        }
        return false;
    }

